sum_ = 0  # заводим переменную счетчик, в которой мы будем считать сумму
n = 1  # текущее натуральное число
max_sum = 250

# TODO переписать на цикл с постусловием
while True:
    if sum_ > max_sum:
        break
    else:
        sum_ += n
        n += 1
    print("Ещё считаю ...", sum_)

print("Количество чисел: ", n)

